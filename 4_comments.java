// This is a single-line comment in Java

/*
 * This is a multi-line comment.
 * It can span multiple lines.
 */

 /**
  * This is a documentation comment.
  * It is used to generate documentation using Javadoc.
  * This class demonstrates different types of comments.
  */
public class CommentExample {

    /**
     * This is the main method which is the entry point of any Java program.
     * @param args Command-line arguments
     */
    public static void main(String[] args) {
        // Print a message to the console
        System.out.println("Demonstrating different types of comments in Java");

        int a = 10; // Declare and initialize an integer variable

        /*
         * Perform a simple arithmetic operation
         */
        int b = a * 2;

        // Print the result
        System.out.println("Value of b (a * 2): " + b);
    }
}
